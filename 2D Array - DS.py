def hourglassSum(a):
    # Write your code here
    abc = []
    for i in range(4):
        for j in range(4):
            abc.append(a[i][j]+a[i][j+1]+a[i][j+2]+a[i+1][j+1]+a[i+2][j]+a[i+2][j+1]+a[i+2][j+2])
    return max(abc)  

